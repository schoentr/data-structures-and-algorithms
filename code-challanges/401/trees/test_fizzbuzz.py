from tree import  BinaryTree, BinarySearchTree

from fizzbuzz import  fizzbuzz

def test_fizzbuzz_one():
   numbers = BinarySearchTree()
   numbers.add(1)
   numbers.add(3)
   numbers.add(28)
   numbers.add(90)
   numbers.add(10)
   numbers.add(18)
   numbers.add(21)
   numbers.add(65)
   numbers.add(100)
   expected = [1,' fizz',10,18,21,28,65,90,100]
   numbers.fizzbuzz()
   actual = numbers.fizzbuzz()
   assert expected == actual
